<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class Product extends Model
{
    protected $fillable = [
        'category_id',
        'name',
        'slug',
        'description',
        'price',
        'active',
        'path',
        'size',
        'stock',
        'price',
    ];

    public function category() { return $this->belongsTo(Category::class); }
}
